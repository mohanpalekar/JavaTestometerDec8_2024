package Loops;

public class ForLoops {
	
	public static void main(String[] args) {
		
		int j = 3;
		
		//table of 3
		
		//start
		//condition
		//steps
		
		//11<=10
		
		int i=0;
		
		for(i=1; i<=10; i=i+1) {
			
			//System.out.println(j*i);
			System.out.println(j+" * "+ i+" = "+ j*i);
			
		}
		
		System.out.println(i);
		
		System.out.println(j);
		
		// 3 * 1 = 3
		
		
	}

}
